def translate_text(text: str, lang_code: str) -> str:
    """
    Translate UI text to the specified language.
    
    Args:
        text: The original English text
        lang_code: The language code (e.g., 'russian', 'german')
    
    Returns:
        Translated text if available, otherwise original text
    """
    translations = UI_TRANSLATIONS.get(lang_code, {})
    return translations.get(text, text)


def set_translations(new_translations: dict):
    """
    Add or update translations dynamically.
    
    Args:
        new_translations: Dictionary of {lang_code: {original: translated}}
    """
    UI_TRANSLATIONS.update(new_translations)
